public class test8 {
    public static void main(String[] args) {
        int a = 15;
        int b = 6;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~b);
        System.out.println(a << 2);
        System.out.println(a >> 2);
        System.out.println(a >>> 2);
    }
}
